#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	char	*dest;
	int		dlen;

	i = -1;
	dlen = ft_strlen(src);
	if (!(dest = (char *)malloc(sizeof(char) * (dlen + 1))))
		return (NULL);
	while (src[++i] != '\0')
		dest[i] = src[i];
	dest[i] = '\0';
	return (dest);
}
