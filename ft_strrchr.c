#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;
	int sl;

	sl = ft_strlen(s) + 1;
	i = sl - 1;
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)(s + i));
		i--;
	}
	return (NULL);
}
